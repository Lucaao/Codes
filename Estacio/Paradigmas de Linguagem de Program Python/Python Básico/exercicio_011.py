altura = int (input("Me diga a altura da parede que deseja pintar: \n")); 
largura = int (input("Me diga a largura da parede que deseja pintar: \n")); 

area = altura * largura; 
pintar = area / 2; 

print (f"A área da sua parede é {area}m2."); 
print (f"E para pintar essa área você precisa de {pintar} litros de tinta."); 